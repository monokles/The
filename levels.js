                 ctx.beginPath();
                 ctx.moveTo(5, -2);
                 ctx.lineTo(0, this.TILE_HEIGHT- 5);
                 ctx.lineTo(this.TILE_WIDTH, this.TILE_HEIGHT- 5);
                 ctx.lineTo(this.TILE_WIDTH - 5, 0);
                 ctx.lineTo(0, 0);
